sc_boolean puzzle_solver_isActive(const Puzzle_solver* handle)
{
	sc_boolean result;
	if (handle->stateConfVector[0] != Puzzle_solver_last_state || handle->stateConfVector[1] != Puzzle_solver_last_state || handle->stateConfVector[2] != Puzzle_solver_last_state || handle->stateConfVector[3] != Puzzle_solver_last_state || handle->stateConfVector[4] != Puzzle_solver_last_state || handle->stateConfVector[5] != Puzzle_solver_last_state)
	{
		result =  bool_true;
	}
	else
	{
		result = bool_false;
	}
	return result;
}

